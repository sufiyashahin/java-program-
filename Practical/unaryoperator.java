package Java;

public class unaryoperator {

	public static void main(String[] args) {
		
		int a=5;
		boolean c=false;
		boolean d=true;
		
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(--a);
		System.out.println(!c);
		System.out.println(!d);
		

	}

}
