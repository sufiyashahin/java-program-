package Java;

public class assignmentoperator {

	public static void main(String[] args) {
		int a=16;
		int b=5;
		System.out.println(a-=b);
		System.out.println(a+=b);
		System.out.println(a*=b);
		System.out.println(a/=b);
		System.out.println(a%=b);
		System.out.println(a=b);
		
	}

}
